function verificarParOuImpar(numero) {
    // Verifica se o número é par
    if (numero % 2 === 0) {
        console.log(`O número ${numero} é par.`);
    } else {
        // Se não for par, é ímpar
        console.log(`O número ${numero} é ímpar.`);
    }
}


verificarParOuImpar(4);  
verificarParOuImpar(11);  
